public class MessageOutOfSyncException extends Exception {

    public MessageOutOfSyncException() {
        super();
    }

    public MessageOutOfSyncException(String message) {
        super(message);
    }

    public MessageOutOfSyncException(String message, Throwable cause) {
        super(message, cause);
    }

    public MessageOutOfSyncException(Throwable cause) {
        super(cause);
    }

}
